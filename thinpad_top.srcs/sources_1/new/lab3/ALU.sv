                32'b??????10000000000000000000000000: alu_y = 25;
                32'b?????100000000000000000000000000: alu_y = 26;
                32'b????1000000000000000000000000000: alu_y = 27;
                32'b???10000000000000000000000000000: alu_y = 28;
                32'b??100000000000000000000000000000: alu_y = 29;
                32'b?1000000000000000000000000000000: alu_y = 30;
                32'b10000000000000000000000000000000: alu_y = 31;
                32'b00000000000000000000000000000000: alu_y = 32;
                default: alu_y = 32;
            endcase
        end else if(alu_op == 12)begin
            alu_y = alu_a & (~alu_b);
        end else if(alu_op == 13)begin
            if(alu_a < alu_b)begin
                alu_y = alu_a;
            end else begin
                alu_y = alu_b;
            end
        end else begin
            alu_y = 0;
        end
    end

endmodule
